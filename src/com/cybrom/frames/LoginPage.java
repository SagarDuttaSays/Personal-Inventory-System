    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String u = t1.getText();
        String p = String.valueOf(p1.getPassword());
        UsersDAO ud = new UsersDAO();
        int x = ud.checkAvailability(u, p);
        if(x==0)
        {
            op1.showMessageDialog(this,"Username does not exists");
        }
        else if(x==1)
        {
            Dashboard d = new Dashboard(u);
            d.setVisible(true);
            this.setVisible(false);
        }
        else if(x==-1)
        {
            op1.showMessageDialog(this,"Invalid username or password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

